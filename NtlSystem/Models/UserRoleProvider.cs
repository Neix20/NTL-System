        public override string[] GetRolesForUser(string username)
        {
            dbNtlSystemEntities db = new dbNtlSystemEntities();

            var user = db.TNtlUsers.FirstOrDefault(it => it.username.Equals(username));
            var userRoles = db.TNtlUserRoles.Where(it => it.user_id == user.id).ToList();
            string[] roleArr = userRoles.Select(it => db.TNtlRoles.Find(it.role_id).name.ToLower()).ToArray();

            return roleArr;
        }

